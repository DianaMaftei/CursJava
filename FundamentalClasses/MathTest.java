public class MathTest {
    public static void main(String...args) {
        
        
        System.out.println(Math.abs(10));
        System.out.println(Math.abs(-20));
        
        
        System.out.println(Math.ceil(3.156));
        System.out.println(Math.floor(3.756));
       
        System.out.println(Math.min(4, 8));
       
        System.out.println(Math.max(234, 567));
        
        System.out.println(Math.round(12.456));
        System.out.println(Math.round(12.756));
        
        System.out.println(Math.pow(2, 3));
        
        System.out.println(Math.sqrt(9));
        
        System.out.println(Math.PI);
        
        System.out.println((int) (Math.random() * 10));
    }
}
